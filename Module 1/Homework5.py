#Работа со списками:
my_list = ['orange', 'banana', 'apple', 'pineapple', 'coconut']
print(my_list)
print(my_list[0])
print(my_list[-1])
print(my_list[2:5])
my_list[2] = 'peach'
print(my_list)

#Работа со словарями:
